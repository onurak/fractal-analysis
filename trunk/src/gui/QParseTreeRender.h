    virtual void mouseMoveEvent(QGraphicsSceneMouseEvent *event)
    {
        emit onMouseEvent(event);
        event->accept();
    }

    virtual void wheelEvent(QGraphicsSceneWheelEvent *event)
    {
        emit onWheelEvent(event);
        event->accept();
    }

